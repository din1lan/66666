function toISO(dateOrISO) {
  if (typeof dateOrISO === 'string') return dateOrISO
  const y = dateOrISO.getFullYear()
  const m = String(dateOrISO.getMonth() + 1).padStart(2, '0')
  const d = String(dateOrISO.getDate()).padStart(2, '0')
  return `${y}-${m}-${d}`
}

export function isWeekend(dateISO) {
  const d = new Date(dateISO + 'T00:00:00')
  const day = d.getDay() // 0=Sun, 6=Sat
  return day === 0 || day === 6
}

export function isExtraHoliday(dateISO) {
  return HOLIDAYS.includes(dateISO)
}

export function isBusinessDay(dateISO) {
  return !isWeekend(dateISO) && !isExtraHoliday(dateISO)
}

// 從 dateISO 往前找最近一個工作日（含 dateISO 本身，若本身已是工作日就直接
// 回傳）。設 60 天安全上限避免陣列/邏輯出錯時無窮迴圈。
export function previousBusinessDay(dateISO) {
  let d = new Date(dateISO + 'T00:00:00')
  for (let i = 0; i < 60; i++) {
    const iso = toISO(d)
    if (isBusinessDay(iso)) return iso
    d.setDate(d.getDate() - 1)
  }
  throw new Error(`previousBusinessDay: 60 天內找不到工作日，請檢查 HOLIDAYS 設定（起始日 ${dateISO}）`)
}
